package com.takisoft.datetimepicker.sample.adpterUtills;

public class SubscriptionAdaterHelper {
    private String Str;
    private int ImgLogo;

    public SubscriptionAdaterHelper(String str, int imgLogo) {
        Str = str;
        ImgLogo = imgLogo;
    }
    public String getStr() {
        return Str;
    }
    public void setStr(String str) {
        Str = str;
    }
    public int getImgLogo() {
        return ImgLogo;
    }
    public void setImgLogo(int imgLogo) {
        ImgLogo = imgLogo;
    }
}
